SELECT lpp.nombre_lugar as 'Nombre Estado', 
	   count(l.tipo_lugar) as 'Cantidad Parroquias'
       FROM LUGAR AS l,
			LUGAR AS lp,
            LUGAR AS lpp
       WHERE lp.tipo_lugar='MUNICIPIO'
			 AND l.fk_lugar = lp.id_lugar
             AND l.tipo_lugar = 'PARROQUIA'
             AND lpp.id_lugar = lp.fk_lugar
             AND lpp.tipo_lugar = 'ESTADO'
       GROUP BY lpp.nombre_lugar;

