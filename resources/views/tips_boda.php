			<li><span class="titulo-lista">ELEGIR EL LUGAR Y LA DECORACIÓN</span>
			<br><br>
			Otra de las claves al momento de realizar una Boda. La elección el <a href="#">Salón o espacio</a> viene condicionada por 
			varios factores, desde la cantidad de invitados hasta el origen de éstos. ¿Es accesible? ¿Se pueden tomar 
			taxis al regreso? ¿Es amplio y cómodo? Además de todo esto, deben prestar atención a detalles como el tec
			ho, la iluminación y los detalles de iluminación, y por supuesto la pintura de las paredes. Son detalles 
			que suelen pasar por alto, pero en la noche de la Boda todo forma parte de un conjunto y no es agradable 
			ver cables colgando, lámparas sin luces y techos descuidados.
			<br><br>
			Con respecto a la <a href="#">decoración</a> pueden elegir los extremos: lo sencillo y lo majestuoso y eso es tan personal 
