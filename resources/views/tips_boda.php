			<li><span class="titulo-lista">PERSONAS IMPORTANTES, FOTOS MEMORABLES</span>
			<br><br>
			Muchas parejas consultan sobre hacer o no fotos de "Mesa". ¿Se usa? ¿No se usa? En lo personal no creemos que se
			trate de lo que está de moda o lo que parece clásico. Consideramos que hay personas muy importantes en la Noche
			de Bodas y es hermoso tener los recuerdos de éstas con nosotros. Abuelos, hermanos y familia cercana son los
			principales.
			<br><br>
			Si no desean hacer "Fotos de Mesa" pueden optar por un espacio de fotos para hacer bellísimos retratos. 
			Es el mejor regalo que podrán hacerle a esos invitados tan importantes para ustedes, y de paso salir un poco 
			de lo "clásico". Y siempre las tendrán con Ustedes. 
