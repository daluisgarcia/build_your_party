			una Ceremonia. Pero también es un día de nervios y apuros.
			<br><br>El mejor consejo: busque un lugar tranquilo, 
			relajado y donde puedan tratarte como una Reina. Un <a href="#">Hotel</a> es la mejor opción: iluminado, con vista a 
