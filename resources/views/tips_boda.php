			<li><span class="titulo-lista">LA NOCHE TAMBIEN ES IMPORTANTE</span>
			<br><br>
			Así como muchos prefieren el día, otros la Noche. Y esto no debe ser una limitación a la hora de organizar 
			tu Boda o decorarla, y mucho menos conseguir un registro Fotográfico completo con condiciones de luz 
			nocturnas.
			<br><br>
			Muchos celebran la Ceremonia Civil en espacios abiertos, en Salones que cuentan con Jardines, pero sucede 
			algo: quien está a cargo de la Iluminación piensa que sólo el Juez debe estar iluminado para poder leer 
			las Actas. Error. La pareja es lo importante, ellos deben estar perfectamente iluminados tanto de espaldas
			 al público como de frente al Juez, y los invitados también deben contar con la iluminación correcta.
			<br><br>
			Busquen y elijan salones y Decoradores que pueden brindarles soluciones en estas situaciones. Su Boda va 
			a lucir mucho mejor, y no es más caro! A veces recursos creativos cambian por completo un espacio y 
			convierten un lugar común en algo mágico.
			<br><br>
			Un salón bien iluminado cambia todo, hace que los detalles se disfruten mucho más y que el registro fotográfico
			 no sufra ciertas limitaciones. Hablen con el responsable de esto, pidan y verifiquen que la Pista de Baile tenga
			  la luz necesaria, y que los espacios estén perfectamente iluminados.
