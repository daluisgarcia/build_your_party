			<li><span class="titulo-lista">EL GRAN SECRETO: LA LUZ</span>
			<br><br>
			Si eligieron casarse de día, la Luz es un factor por demás importante. Si eligieron un salón, aprovechen este
			recurso y dejen entrar la luz natural al espacio de la Boda. 
			<br><br>
			De nada sirve organizar una Boda en pleno día o por la tarde si el salón o los espacios de la Boda son 
			cerrados y no dejan ingresar la suficiente cantidad de Luz. La magia se pierde.
			<br><br>
			Muchas parejas hablan de ciertas limitaciones para casarse de día, pero no son tales. Muchas Bodas están 
			comenzando por la tarde temprano y terminan entrada la madrugada y se disfrutan el doble! Además dan la oportunidad 
			de un registro fotográfico más amplio y con muchísimos matices.
